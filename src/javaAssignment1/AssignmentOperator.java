package javaAssignment1;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a =5;//    // initializing variables
        a+=6; //a=a+6
        System.out.println(a);


        a-=4; //a=a-4
        System.out.println(a);

        a*=3; //a =a*3
        System.out.println(a);

    }

}
