package javaAssignment1;

public class RelationalOperators {

    public static void main(String[] args) {
        System.out.println(16>50);
        System.out.println(16>=50);
        System.out.println(16<50);
        System.out.println(16<=50);
        System.out.println(16==50);
        System.out.println(16!=50);


    }
}
